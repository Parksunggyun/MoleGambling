package al.tong.mon.molegambling;

public class QuestItem {

    private int seq;
    private int color;

    public QuestItem(int seq, int color) {
        this.seq = seq;
        this.color = color;
    }

    public int getSeq() {
        return seq;
    }

    public int getColor() {
        return color;
    }
}
